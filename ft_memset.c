#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*x;

	i = 0;
	x = (char *) b;
	while (i < len)
	{
		x[i] = c;
		i++;
	}
	return (b);
}
